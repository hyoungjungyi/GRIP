import React, { useEffect, useState } from "react";
import { useNavigate } from "react-router-dom";
import {
  getAllSongLists,
  generateTabFromAudio,
  uploadSheet,
} from "./sheetViewApi";
import styles from "./SheetView.module.css";

interface OngoingSong {
  song_id: number;
  title: string;
  artist: string;
  progress: number;
}
interface RecommendSong {
  song_id: number;
  title: string;
  artist: string;
  genre: string;
}
interface GeneratedSong {
  song_id: number;
  title: string;
  created_at: string;
}
interface SongLists {
  ongoing: OngoingSong[];
  recommend: RecommendSong[];
  generated: GeneratedSong[];
}

const SheetView: React.FC = () => {
  const [link, setLink] = useState("");
  const [lists, setLists] = useState<SongLists | null>(null);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(false);
  const [showUploadPopup, setShowUploadPopup] = useState(false);
  const [uploadData, setUploadData] = useState({
    title: "",
    artist: "",
    cover: null as File | null,
    noteSheet: null as File | null,
    tabSheet: null as File | null,
  });
  const [isUploading, setIsUploading] = useState(false);
  const navigate = useNavigate();

  useEffect(() => {
    const fetchSongLists = async () => {
      try {
        const data = await getAllSongLists();
        console.log("[üìã SheetView] API Response:", data);
        console.log("[üìã SheetView] Ongoing songs:", data?.ongoing);
        console.log("[üìã SheetView] Recommend songs:", data?.recommend);
        console.log("[üìã SheetView] Generated songs:", data?.generated);
        setLists(data);
      } catch (error) {
        console.error("Error fetching song lists:", error);
        setError(true);
      } finally {
        setLoading(false);
      }
    };

    fetchSongLists();
  }, []);

  const handleGenerate = async () => {
    if (!link) {
      alert("ÎßÅÌÅ¨Î•º ÏûÖÎ†•ÌïòÏÑ∏Ïöî.");
      return;
    }

    try {
      const data = await generateTabFromAudio(link);
      alert(
        `ÏÉùÏÑ± ÏôÑÎ£å! song_id: ${data.song_id}\nTab Ïù¥ÎØ∏ÏßÄ: ${data.tab_image_url}`
      );
      // ÌïÑÏöîÏãú: ÏÉùÏÑ±Îêú song_idÎ°ú Ïù¥ÎèôÌïòÍ±∞ÎÇò, UI Í∞±Ïã† Îì± Ï∂îÍ∞Ä ÏûëÏóÖ
    } catch (error) {
      console.error("Tab generation failed:", error);
      alert("ÏÉùÏÑ± Ïã§Ìå®: " + error);
    }
  };

  const handleAlbumClick = (songId: number) => {
    navigate(`/album/${songId}`);
  };

  const handleUploadClick = () => {
    setShowUploadPopup(true);
  };

  const handleUploadClose = () => {
    setShowUploadPopup(false);
    setUploadData({
      title: "",
      artist: "",
      cover: null,
      noteSheet: null,
      tabSheet: null,
    });
    setIsUploading(false);
  };

  const handleFileChange = (field: string, file: File | null) => {
    setUploadData((prev) => ({
      ...prev,
      [field]: file,
    }));
  };

  const handleInputChange = (field: string, value: string) => {
    setUploadData((prev) => ({
      ...prev,
      [field]: value,
    }));
  };

  const handleUploadSubmit = async () => {
    const { title, artist, cover, noteSheet, tabSheet } = uploadData;

    if (!title.trim() || !artist.trim() || !cover || !noteSheet || !tabSheet) {
      alert("Î™®Îì† ÌïÑÎìúÎ•º ÏûÖÎ†•Ìï¥Ï£ºÏÑ∏Ïöî.");
      return;
    }

    setIsUploading(true);
    try {
      const uploadResult = await uploadSheet({
        title: title.trim(),
        artist: artist.trim(),
        cover,
        noteSheet,
        tabSheet,
      });

      console.log("[‚úÖ SheetView] Upload success:", uploadResult);

      alert("ÏïÖÎ≥¥Í∞Ä ÏÑ±Í≥µÏ†ÅÏúºÎ°ú ÏóÖÎ°úÎìúÎêòÏóàÏäµÎãàÎã§!");
      handleUploadClose();

      // Î¶¨Ïä§Ìä∏ ÏÉàÎ°úÍ≥†Ïπ®
      console.log("[üîÑ SheetView] Refreshing song lists...");
      const data = await getAllSongLists();
      console.log("[üìã SheetView] Refreshed data:", data);
      setLists(data);
    } catch (error) {
      console.error("ÏóÖÎ°úÎìú Ïã§Ìå®:", error);
      alert("ÏóÖÎ°úÎìúÏóê Ïã§Ìå®ÌñàÏäµÎãàÎã§. Îã§Ïãú ÏãúÎèÑÌï¥Ï£ºÏÑ∏Ïöî.");
    } finally {
      setIsUploading(false);
    }
  };

  const albumCover = "/guitar_head.png"; // Í∏∞Î≥∏ Ïù¥ÎØ∏ÏßÄ

  // Ïï®Î≤î Ïª§Î≤Ñ Ïù¥ÎØ∏ÏßÄ URL Í∞ÄÏ†∏Ïò§Í∏∞ Ìï®Ïàò
  const getAlbumCoverUrl = (song: any) => {
    if (song.coverUrl) {
      console.log(`[üñºÔ∏è SheetView] Found cover image: coverUrl = ${song.coverUrl}`);
      return song.coverUrl;
    }
    
    console.log(`[üñºÔ∏è SheetView] No cover image found for song:`, song);
    return albumCover; // Í∏∞Î≥∏ Ïù¥ÎØ∏ÏßÄ Î∞òÌôò
  };

  return (
    <>
      <div className={styles.container}>
        <div className={styles.topContainer}>
          <div className={styles.topDescription}>
            <span className={styles.topDescriptionText}>
              Generate Tab Sheet
            </span>
          </div>
          {/* Link input & Generate button */}
          <div className={styles.inputRow}>
            <input
              type="text"
              value={link}
              onChange={(e) => setLink(e.target.value)}
              placeholder="Paste a link here..."
              className={styles.input}
            />
            <button onClick={handleGenerate} className={styles.glassButton}>
              <img src="../src/assets/ai.png" alt="AI" className={styles.aiBadgeImage} />
              <span>GENERATE</span>
            </button>
            <button onClick={handleUploadClick} className={styles.button}>
              UPLOAD SHEET
            </button>
          </div>
        </div>
        <div className={styles.bottomContainer}>
          {/* Album lists */}
          {loading ? (
            <div>
              {/* Ongoing */}
              <div className={styles.listSection}>
                <h2 className={styles.listTitle}>Ongoing</h2>
                <div className={styles.albumRow}>
                  {Array.from({ length: 3 }).map((_, idx) => (
                    <div key={idx} className={styles.albumCard}>
                      <img
                        src={albumCover}
                        alt="cover"
                        className={styles.albumImg}
                      />
                      <div className={styles.failedMsg}>Loading...</div>
                    </div>
                  ))}
                </div>
              </div>
              {/* Recommend */}
              <div className={styles.listSection}>
                <h2 className={styles.listTitle}>Recommend</h2>
                <div className={styles.albumRow}>
                  {Array.from({ length: 3 }).map((_, idx) => (
                    <div key={idx} className={styles.albumCard}>
                      <img
                        src={albumCover}
                        alt="cover"
                        className={styles.albumImg}
                      />
                      <div className={styles.failedMsg}>Loading...</div>
                    </div>
                  ))}
                </div>
              </div>
              {/* Generated */}
              <div className={styles.listSection}>
                <h2 className={styles.listTitle}>Generated</h2>
                <div className={styles.albumRow}>
                  {Array.from({ length: 3 }).map((_, idx) => (
                    <div key={idx} className={styles.albumCard}>
                      <img
                        src={albumCover}
                        alt="cover"
                        className={styles.albumImg}
                      />
                      <div className={styles.failedMsg}>Loading...</div>
                    </div>
                  ))}
                </div>
              </div>
            </div>
          ) : (
            <div>
              {/* Ongoing */}
              <div className={styles.listSection}>
                <h2 className={styles.listTitle}>Ongoing</h2>
                <div className={styles.albumRow}>
                  {error ? (
                    Array.from({ length: 3 }).map((_, idx) => (
                      <div
                        key={idx}
                        className={styles.albumCard}
                        onClick={() => handleAlbumClick(0)}
                        style={{ cursor: "pointer" }}
                      >
                        <img
                          src={albumCover}
                          alt="cover"
                          className={styles.albumImg}
                        />
                        <div className={styles.failedMsg}>Failed to load</div>
                      </div>
                    ))
                  ) : lists && lists.ongoing.length === 0 ? (
                    <div>No ongoing songs.</div>
                  ) : (
                    lists &&
                    lists.ongoing.map((song) => (
                      <div
                        key={song.song_id}
                        className={styles.albumCard}
                        onClick={() => handleAlbumClick(song.song_id)}
                        style={{ cursor: "pointer" }}
                      >
                        <img
                          src={getAlbumCoverUrl(song)}
                          alt="cover"
                          className={styles.albumImg}
                        />
                        <div className={styles.albumTitle}>{song.title}</div>
                        <div className={styles.albumArtist}>{song.artist}</div>
                        <div className={styles.albumProgress}>
                          Progress: {song.progress}%
                        </div>
                      </div>
                    ))
                  )}
                </div>
              </div>
              {/* Recommend */}
              <div className={styles.listSection}>
                <h2 className={styles.listTitle}>Recommend</h2>
                <div className={styles.albumRow}>
                  {error ? (
                    Array.from({ length: 3 }).map((_, idx) => (
                      <div
                        key={idx}
                        className={styles.albumCard}
                        onClick={() => handleAlbumClick(0)}
                        style={{ cursor: "pointer" }}
                      >
                        <img
                          src={albumCover}
                          alt="cover"
                          className={styles.albumImg}
                        />
                        <div className={styles.failedMsg}>Failed to load</div>
                      </div>
                    ))
                  ) : lists && lists.recommend.length === 0 ? (
                    <div>No recommendations.</div>
                  ) : (
                    lists &&
                    lists.recommend.map((song) => (
                      <div
                        key={song.song_id}
                        className={styles.albumCard}
                        onClick={() => handleAlbumClick(song.song_id)}
                        style={{ cursor: "pointer" }}
                      >
                        <img
                          src={getAlbumCoverUrl(song)}
                          alt="cover"
                          className={styles.albumImg}
                        />
                        <div className={styles.albumTitle}>{song.title}</div>
                        <div className={styles.albumArtist}>{song.artist}</div>
                        <div className={styles.albumGenre}>{song.genre}</div>
                      </div>
                    ))
                  )}
                </div>
              </div>
              {/* Generated */}
              <div className={styles.listSection}>
                <h2 className={styles.listTitle}>Generated</h2>
                <div className={styles.albumRow}>
                  {error ? (
                    Array.from({ length: 3 }).map((_, idx) => (
                      <div
                        key={idx}
                        className={styles.albumCard}
                        onClick={() => handleAlbumClick(0)}
                        style={{ cursor: "pointer" }}
                      >
                        <img
                          src={albumCover}
                          alt="cover"
                          className={styles.albumImg}
                        />
                        <div className={styles.failedMsg}>Failed to load</div>
                      </div>
                    ))
                  ) : lists && lists.generated.length === 0 ? (
                    <div>No generated songs.</div>
                  ) : (
                    lists &&
                    lists.generated.map((song) => (
                      <div
                        key={song.song_id}
                        className={styles.albumCard}
                        onClick={() => handleAlbumClick(song.song_id)}
                        style={{ cursor: "pointer" }}
                      >
                        <img
                          src={getAlbumCoverUrl(song)}
                          alt="cover"
                          className={styles.albumImg}
                        />
                        <div className={styles.albumTitle}>{song.title}</div>
                        <div className={styles.albumDate}>
                          {new Date(song.created_at).toLocaleString()}
                        </div>
                      </div>
                    ))
                  )}
                </div>
              </div>
            </div>
          )}
        </div>
      </div>

      {/* ÏóÖÎ°úÎìú ÌåùÏóÖ */}
      {showUploadPopup && (
        <div className={styles.popupOverlay}>
          <div className={styles.popupBox}>
            <div className={styles.popupHeader}>
              <h3>ÏïÖÎ≥¥ ÏóÖÎ°úÎìú</h3>
              <button
                className={styles.popupClose}
                onClick={handleUploadClose}
                disabled={isUploading}
              >
                √ó
              </button>
            </div>

            <div className={styles.popupContent}>
              {/* Ï†úÎ™© ÏûÖÎ†• */}
              <div className={styles.inputGroup}>
                <label>Í≥° Ï†úÎ™© *</label>
                <input
                  type="text"
                  value={uploadData.title}
                  onChange={(e) => handleInputChange("title", e.target.value)}
                  placeholder="Í≥° Ï†úÎ™©ÏùÑ ÏûÖÎ†•ÌïòÏÑ∏Ïöî"
                  disabled={isUploading}
                />
              </div>

              {/* ÏïÑÌã∞Ïä§Ìä∏ ÏûÖÎ†• */}
              <div className={styles.inputGroup}>
                <label>ÏïÑÌã∞Ïä§Ìä∏ *</label>
                <input
                  type="text"
                  value={uploadData.artist}
                  onChange={(e) => handleInputChange("artist", e.target.value)}
                  placeholder="ÏïÑÌã∞Ïä§Ìä∏Î•º ÏûÖÎ†•ÌïòÏÑ∏Ïöî"
                  disabled={isUploading}
                />
              </div>

              {/* ÌëúÏßÄ ÏóÖÎ°úÎìú */}
              <div className={styles.inputGroup}>
                <label>ÌëúÏßÄ Ïù¥ÎØ∏ÏßÄ *</label>
                <input
                  type="file"
                  accept="image/*"
                  onChange={(e) =>
                    handleFileChange("cover", e.target.files?.[0] || null)
                  }
                  disabled={isUploading}
                />
                {uploadData.cover && (
                  <div className={styles.fileInfo}>
                    ÏÑ†ÌÉùÎêú ÌååÏùº: {uploadData.cover.name}
                  </div>
                )}
              </div>

              {/* Note ÏïÖÎ≥¥ ÏóÖÎ°úÎìú */}
              <div className={styles.inputGroup}>
                <label>Note ÏïÖÎ≥¥ *</label>
                <input
                  type="file"
                  accept="image/*"
                  onChange={(e) =>
                    handleFileChange("noteSheet", e.target.files?.[0] || null)
                  }
                  disabled={isUploading}
                />
                {uploadData.noteSheet && (
                  <div className={styles.fileInfo}>
                    ÏÑ†ÌÉùÎêú ÌååÏùº: {uploadData.noteSheet.name}
                  </div>
                )}
              </div>

              {/* Tab ÏïÖÎ≥¥ ÏóÖÎ°úÎìú */}
              <div className={styles.inputGroup}>
                <label>Tab ÏïÖÎ≥¥ *</label>
                <input
                  type="file"
                  accept="image/*"
                  onChange={(e) =>
                    handleFileChange("tabSheet", e.target.files?.[0] || null)
                  }
                  disabled={isUploading}
                />
                {uploadData.tabSheet && (
                  <div className={styles.fileInfo}>
                    ÏÑ†ÌÉùÎêú ÌååÏùº: {uploadData.tabSheet.name}
                  </div>
                )}
              </div>

              {/* ÏóÖÎ°úÎìú Î≤ÑÌäº */}
              <div className={styles.buttonGroup}>
                <button
                  className={styles.cancelBtn}
                  onClick={handleUploadClose}
                  disabled={isUploading}
                >
                  Ï∑®ÏÜå
                </button>
                <button
                  className={styles.uploadBtn}
                  onClick={handleUploadSubmit}
                  disabled={
                    isUploading ||
                    !uploadData.title.trim() ||
                    !uploadData.artist.trim() ||
                    !uploadData.cover ||
                    !uploadData.noteSheet ||
                    !uploadData.tabSheet
                  }
                >
                  {isUploading ? "ÏóÖÎ°úÎìú Ï§ë..." : "ÏóÖÎ°úÎìú"}
                </button>
              </div>
            </div>
          </div>
        </div>
      )}
    </>
  );
};

export default SheetView;
